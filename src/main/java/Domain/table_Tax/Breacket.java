package Domain.table_Tax;

public class Breacket {
    
    private Double min;
    private Double max;
    private double rate;

    public Breacket(){}

    public Double getMin(){
        return min;
    }

    public void setMain(Double min){
        this.min = min;
    }

    public Double getMax(){
        return max;
    }

    public void setMax(Double max){
        this.max = max;
    }

    public double getRate(){
        return rate;
    }

    public void setRate(double rate){
        this.rate = rate;
    }
    
}


